"""Dashboard renderer: outputs JSON, Markdown, and static HTML."""
import json, sys
from dataclasses import asdict
from pathlib import Path
sys.path.insert(0, str(Path(__file__).resolve().parent.parent.parent / "src"))


class DashboardRenderer:
    def __init__(self, forbidden_fields: list = None):
        self.forbidden = forbidden_fields or []

    def render_json(self, dashboard) -> dict:
        return asdict(dashboard)

    def render_markdown(self, dashboard, brief) -> str:
        bs = dashboard.bankroll_summary
        cs = dashboard.calendar_summary
        ps = dashboard.parlay_summary
        fs = dashboard.futures_summary
        ss = dashboard.settlement_summary

        lines = [
            "# Campaign Dashboard",
            "",
            f"**Date:** {dashboard.current_date} | **Mode:** {dashboard.dashboard_mode}",
            "",
            "## Campaign State",
            f"- State: {bs['bankroll_state']}",
            f"- Liquid: {bs['liquid_simulated_bankroll']} CNY",
            f"- Locked Pending: {bs['locked_pending_units']} CNY",
            f"- Total Equity: {bs['total_campaign_equity']} CNY",
            f"- Target: {bs['target_bankroll']} CNY",
            f"- Multiplier (Liquid): {bs['required_multiplier_liquid']}x",
            f"- Multiplier (Equity): {bs['required_multiplier_equity']}x",
            "",
            "## Calendar",
            f"- Stage: {cs.get('stage','')} | Mode: {cs.get('daily_mode','')}",
            f"- Matches Today: {cs.get('match_count',0)} | Remaining: {cs.get('remaining_matches',0)}",
            "",
            "## Bucket Summary",
        ]
        for b, info in (dashboard.bucket_summary or {}).items():
            lines.append(f"- **{b}**: {info.get('budget',0)} CNY, {info.get('candidate_count',0)} candidates")
        lines.append("")
        lines.append("## Parlay")
        lines.append(f"- Candidates: {ps.get('source_candidates',0)} -> Combos: {ps.get('ranked_count',0)} (blocked: {ps.get('blocked_count',0)})")
        lines.append(f"- Edge: {ps.get('edge_parlays',0)} | Attack: {ps.get('attack_parlays',0)}")
        lines.append("")
        lines.append("## Futures")
        lines.append(f"- Groups: {fs.get('groups_simulated',0)} | Odds: {fs.get('futures_odds_count',0)} | Bucket: {fs.get('futures_bucket',0)}")
        lines.append("")
        lines.append("## Settlement")
        lines.append(f"- Settled: {ss.get('settled',0)} | Pending: {ss.get('pending',0)} | Hit: {ss.get('hit',0)}")
        lines.append(f"- Routing: {ss.get('routing_hint','')}")
        lines.append("")
        lines.append("## Safety")
        for k, v in dashboard.safety.items():
            lines.append(f"- **{k}:** {v}")
        lines.append("")
        lines.append("*Generated by Campaign Dashboard v1*")
        return chr(10).join(lines)

