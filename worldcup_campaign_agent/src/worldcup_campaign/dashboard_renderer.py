    def write_outputs(self, dashboard, brief, output_dir: str) -> dict:
        od = Path(output_dir)
        od.mkdir(parents=True, exist_ok=True)

        paths = {}
        paths["json"] = str(od / "campaign_dashboard.json")
        paths["md"] = str(od / "daily_brief.md")
        paths["html"] = str(od / "campaign_dashboard.html")

        Path(paths["json"]).write_text(json.dumps(self.render_json(dashboard), indent=2, ensure_ascii=False, default=str), encoding="utf-8")
        Path(paths["md"]).write_text(self.render_markdown(dashboard, brief), encoding="utf-8")
        Path(paths["html"]).write_text(self.render_html(dashboard, brief), encoding="utf-8")

        return paths

    def validate_no_forbidden(self, payload: dict) -> None:
        """Check for forbidden actionable fields (not safety flags)."""
        actionable_forbidden = ["stake", "stake_amount", "stake_to_match",
                               "bet_instruction", "bookmaker", "bookmaker_account",
                               "real_money_balance"]
        def check_keys(d, path=""):
            if isinstance(d, dict):
                for k, v in d.items():
                    if k in actionable_forbidden:
                        raise ValueError(f"Forbidden actionable field '{k}' found at {path}")
                    check_keys(v, f"{path}.{k}" if path else k)
        check_keys(payload, "")
